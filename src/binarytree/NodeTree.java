public class NodeTree {
    int data;
    NodeTree left;
    NodeTree right;

    public NodeTree(int data) {
        this.data = data;
        this.left = left;
        this.right = right;
    }
    
    public NodeTree() {
        this.data = data;
        this.left= left;
        this.right = right;
    }

    public NodeTree getLeft() {
        return left;
    }
    public NodeTree getRight() {
        return right;
    }
    
}